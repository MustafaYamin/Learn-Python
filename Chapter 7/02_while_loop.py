# lets say we have a variable i

i = 1

# Now we create a while loop 
while (i < 51):
    print(i)
    i +=1

# Here the while loop will first check the condition and if the condition is true 
# it will run the loop where we wrote that first print the value of i
# then increment 1 in the value of i. After that it will again go and check if the condition is true or false 
# if the condition is true it will repeat the task and will stop only when the condition gets false
