# You can use a for loop to iterate list, tupple, and strings as well

# lets say we have a tupple
print("---------------------------------------------")
t = ("Mustafa", 564, True, 9.879, 'yes')

for i in t:
    print(i) 

print('---------------------------------------------')
# Lets do same with a list
l = [78, 986, 1.432, 323, 4, 72 ]

for i in l:
    print(i)

print('---------------------------------------------')

# Now lets do with string
s = "Mustafa"
for i in s:
    print(i)