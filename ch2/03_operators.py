# Operaters
# Operators are used to perform operations on variables and values.

# Arthmatic operators
# +, -, *, /

a = 1
b = 2 
c = a + b # here + is an arthmatic operator
# print(c)

# Assignment operators
# =, +=, -=, 

d = 1 #here = is an assignment operator saying d is equal to 1
e = 5
e += 3 #here += is the assignment operator saying to add 3 in the value of e which was 5
# print(e)

# Comparison operators
# ==, !=, >, <, >=, <=
# Comparison operators are used to compare two values
# and they will always give a boolean value as a result.

f = 5 > 10 # it is comparing 5 and 10 and the result will be False
# print(f)

f = 5 == 5 # it is comparing 5 and 5 and the result will be True
# print(f)

f = 5 != 5 # it is comparing 5 and 5 and the result will be False
# print(f)

# Logical operators
# and, or, not

# Truth table for and operator
# and only returns True if both the values are True

print(True and True) # True
print(True and False) # False
print(False and True) # False
print(False and False) # False

# Truth table for or operator
# or returns True if any of the values is True

print(True or True) # True
print(True or False) # True
print(False or True) # True
print(False or False) # False

# Truth table for not operator
# not returns the opposite of the value

print(not True) # False
print(not False) # True

