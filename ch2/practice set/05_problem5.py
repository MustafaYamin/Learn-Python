# Q5
# Calculates the average of two numbers
print("Q5: Enter two values to calculate their average")

# Allows user to enter first value
num1 = input("Enter first value: ")

# Changes the data type of first value
value1 = int(num1)

# Allows user to enter second value
num2 = input("Enter second value: ")

# Changes the data type of second value
value2 = int(num2)

# Calculates the average of the two values
avg = (value1 + value2) / 2

# Prints the average of the two values
print("Q5: Average of given values is: ", avg)