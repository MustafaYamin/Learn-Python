# Lets talk about different methods in sets

c = {"Mustafa", 9, 78, True, 0.65}

# add method
# If I want to add Pakistan in my set I can use add method
c.add("Pakistan")
print(c, type(c))

# You can also print the length of a set
print(len(c)) #Output: 6

# we can use remove method to delete/remove an item fom the set
c.remove(True)
print(c)

